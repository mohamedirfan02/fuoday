class DepartmentMemberEntity {
  final int webUserId;
  final String name;
  final String empId;
  final String email;
  final String role;
  final String department;
  final String designation;

  DepartmentMemberEntity({
    required this.webUserId,
    required this.name,
    required this.empId,
    required this.email,
    required this.role,
    required this.department,
    required this.designation,
  });
}
